// ─── Dimensions ──────────────────────────────────────────────────────────────

function dimensionsCssPlugin() {
  return {
    name: 'het-dimensions-css',
    /** @param {{ tokens: Record<string, TerrazzoToken>, outputFile: (filename: string, contents: string) => void }} ctx */
    async build({ tokens, outputFile }) {
      const { filename, contents } = makeCssOutput({
        tokens: Object.values(tokens).filter(isDim),
        filename: 'dimensions.css',
        srcName: 'dimensions',
        // Must NOT use @theme inline — Tailwind needs literal breakpoint values to
        // emit @media (min-width: 600px) queries. var() in media queries is invalid CSS.
        inlineTheme: false,
      })
      outputFile(filename, contents)
    },
  }
}

function dimensionsTsPlugin() {
  return {
    name: 'het-dimensions-ts',
    /** @param {{ tokens: Record<string, TerrazzoToken>, outputFile: (filename: string, contents: string) => void }} ctx */
    async build({ tokens, outputFile }) {
      const dimTokens = Object.values(tokens).filter(isDim)
      const bpTokens = dimTokens.filter(isBreakpoint)

      const bpEntries = bpTokens
        .map((t) => `  ${idToLeafKey(t.id)}: '${valueToString(t.$value)}'`)
        .join(',\n')

      const extra = `
export const breakpointValues = {
${bpEntries},
} as const

export type Breakpoint = keyof typeof breakpointValues
`
      const { filename, contents } = makeTsOutput({
        tokens: dimTokens,
        keyFn: idToCamelKey,
        exportBase: 'dimension',
        filename: 'dimensions.ts',
        srcName: 'dimensions',
        extra,
      })
      outputFile(filename, contents)
    },
  }
}

